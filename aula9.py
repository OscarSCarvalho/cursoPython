

#Aula if  else elif

entrada = input( ' Voce quer entrar  "sim" "não" ?  ')

if entrada ==  "sim":    #(if) >>> si  condiçao que compara resposta do ususrio
    print('Bem vindo ')

elif entrada == "não":     #(elif) cndiçao que compara um opçao secundaria do susario
    print('finalizado')  

else:
    print('voce nao digitou nenhuma opção')  # else resposta final do ususario    

    